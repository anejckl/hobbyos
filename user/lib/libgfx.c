/* Syscall helpers */
static inline uint64_t _sc0(uint64_t n) {
    uint64_t r;
    __asm__ volatile("int $0x80":"=a"(r):"a"(n):"rcx","r11","memory");
    return r;
}
static inline uint64_t _sc1(uint64_t n, uint64_t a1) {
    uint64_t r;
    __asm__ volatile("int $0x80":"=a"(r):"a"(n),"D"(a1):"rcx","r11","memory");
    return r;
}
static inline uint64_t _sc2(uint64_t n, uint64_t a1, uint64_t a2) {
    uint64_t r;
    __asm__ volatile("int $0x80":"=a"(r):"a"(n),"D"(a1),"S"(a2):"rcx","r11","memory");
    return r;
}
static inline uint64_t _sc3(uint64_t n, uint64_t a1, uint64_t a2, uint64_t a3) {
    uint64_t r;
    __asm__ volatile("int $0x80":"=a"(r):"a"(n),"D"(a1),"S"(a2),"d"(a3):"rcx","r11","memory");
    return r;
}

struct _mmap_args {
    uint64_t addr;
    uint64_t len;
    uint32_t prot;
    uint32_t flags;
    int32_t  fd;
    uint32_t pad;
    uint64_t offset;
};

static void *_mmap(void *addr, uint64_t len, uint32_t prot, uint32_t flags,
                   int32_t fd, uint64_t offset) {
    struct _mmap_args args;
    args.addr = (uint64_t)addr;
    args.len = len;
    args.prot = prot;
    args.flags = flags;
    args.fd = fd;
    args.pad = 0;
    args.offset = offset;
    return (void *)_sc1(SYS_MMAP, (uint64_t)&args);
}

/* Simple malloc using brk — just allocate from static pool */
static uint8_t gfx_heap[8 * 1024 * 1024];  /* 8 MB static pool */
static uint64_t gfx_heap_ptr = 0;

static void *gfx_malloc(uint64_t size) {
    size = (size + 7) & ~7ULL;
    if (gfx_heap_ptr + size > sizeof(gfx_heap)) return (void *)0;
    void *p = &gfx_heap[gfx_heap_ptr];
    gfx_heap_ptr += size;
    return p;
}

