/* ---- Alpha blending ---- */

static inline uint32_t gfx_blend(uint32_t dst, uint32_t src, uint8_t alpha) {
    uint32_t inv = 255 - alpha;
    uint32_t sr = (src >> 16) & 0xFF, sg = (src >> 8) & 0xFF, sb = src & 0xFF;
    uint32_t dr = (dst >> 16) & 0xFF, dg = (dst >> 8) & 0xFF, db = dst & 0xFF;
    uint32_t r = (sr * alpha + dr * inv) / 255;
    uint32_t g = (sg * alpha + dg * inv) / 255;
    uint32_t b = (sb * alpha + db * inv) / 255;
    return (r << 16) | (g << 8) | b;
}

void gfx_surface_fill_rect_alpha(gfx_surface_t *s, int x, int y, int w, int h,
                                   uint32_t color, uint8_t alpha) {
    if (!s || !s->data) return;
    int x1 = x, y1 = y, x2 = x + w, y2 = y + h;
    if (x1 < 0) x1 = 0;
    if (y1 < 0) y1 = 0;
    if (x2 > (int)s->width)  x2 = (int)s->width;
    if (y2 > (int)s->height) y2 = (int)s->height;
    for (int py = y1; py < y2; py++) {
        uint32_t *row = &s->data[(uint64_t)py * s->width];
        for (int px = x1; px < x2; px++)
            row[px] = gfx_blend(row[px], color, alpha);
    }
}

