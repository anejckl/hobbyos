int gfx_init(gfx_ctx_t *ctx) {
    if (!ctx) return -1;

    /* Open /dev/fb0 */
    int64_t fd = (int64_t)_sc2(SYS_OPEN, (uint64_t)"/dev/fb0", 0);
    if (fd < 0) return -1;

    /* Get framebuffer info via ioctl */
    struct fb_info info;
    if ((int64_t)_sc3(SYS_IOCTL, (uint64_t)fd, FBIOGET_INFO, (uint64_t)&info) != 0) {
        _sc1(SYS_CLOSE, (uint64_t)fd);
        return -1;
    }

    /* mmap the framebuffer */
    uint64_t fb_size = (uint64_t)info.pitch * info.height;
    void *pixels = _mmap(0, fb_size, 3 /* PROT_READ|PROT_WRITE */,
                         1 /* MAP_SHARED */, (int32_t)fd, 0);
    _sc1(SYS_CLOSE, (uint64_t)fd);

    if (pixels == (void *)(uint64_t)-1) return -1;

    ctx->pixels = (uint32_t *)pixels;
    ctx->width  = info.width;
    ctx->height = info.height;
    ctx->pitch  = info.pitch;
    ctx->bpp    = info.bpp;
    return 0;
}

void gfx_fini(gfx_ctx_t *ctx) {
    if (!ctx || !ctx->pixels) return;
    uint64_t fb_size = (uint64_t)ctx->pitch * ctx->height;
    _sc2(SYS_MUNMAP, (uint64_t)ctx->pixels, fb_size);
    ctx->pixels = (uint32_t *)0;
}

void gfx_clear(gfx_ctx_t *ctx, uint32_t color) {
    if (!ctx || !ctx->pixels) return;
    /* Use 64-bit word stores to fill faster (no SSE) */
    uint64_t pattern = ((uint64_t)color << 32) | color;
    uint64_t *dst = (uint64_t *)ctx->pixels;
    uint64_t words = (uint64_t)(ctx->pitch / 4) * ctx->height / 2;
    for (uint64_t i = 0; i < words; i++)
        dst[i] = pattern;
}

void gfx_put_pixel(gfx_ctx_t *ctx, int x, int y, uint32_t color) {
    if (!ctx || !ctx->pixels) return;
    if (x < 0 || y < 0 || (uint32_t)x >= ctx->width || (uint32_t)y >= ctx->height) return;
    uint32_t *row = (uint32_t *)((uint8_t *)ctx->pixels + (uint64_t)y * ctx->pitch);
    row[x] = color;
}

void gfx_fill_rect(gfx_ctx_t *ctx, int x, int y, int w, int h, uint32_t color) {
    if (!ctx || !ctx->pixels) return;
    int x1 = x, y1 = y, x2 = x + w, y2 = y + h;
    if (x1 < 0) x1 = 0;
    if (y1 < 0) y1 = 0;
    if (x2 > (int)ctx->width)  x2 = (int)ctx->width;
    if (y2 > (int)ctx->height) y2 = (int)ctx->height;
    for (int py = y1; py < y2; py++) {
        uint32_t *row = (uint32_t *)((uint8_t *)ctx->pixels + (uint64_t)py * ctx->pitch);
        for (int px = x1; px < x2; px++)
            row[px] = color;
    }
}

void gfx_draw_line(gfx_ctx_t *ctx, int x0, int y0, int x1, int y1, uint32_t color) {
    int dx = x1 - x0; if (dx < 0) dx = -dx;
    int dy = y1 - y0; if (dy < 0) dy = -dy;
    int sx = (x0 < x1) ? 1 : -1;
    int sy = (y0 < y1) ? 1 : -1;
    int err = dx - dy;
    while (1) {
        gfx_put_pixel(ctx, x0, y0, color);
        if (x0 == x1 && y0 == y1) break;
        int e2 = 2 * err;
        if (e2 > -dy) { err -= dy; x0 += sx; }
        if (e2 <  dx) { err += dx; y0 += sy; }
    }
}

void gfx_draw_char(gfx_ctx_t *ctx, int x, int y, char c, uint32_t fg, uint32_t bg) {
    if (!ctx) return;
    int idx = (int)(unsigned char)c - 0x20;
    if (idx < 0 || idx >= 95) idx = 0;
    const uint8_t *glyph = font8x8[idx];
    for (int row = 0; row < 8; row++) {
        uint8_t bits = glyph[row];
        for (int col = 0; col < 8; col++) {
            uint32_t color = (bits & (1 << col)) ? fg : bg;
            gfx_put_pixel(ctx, x + col, y + row, color);
        }
    }
}

void gfx_draw_string(gfx_ctx_t *ctx, int x, int y, const char *s,
                     uint32_t fg, uint32_t bg) {
    if (!ctx || !s) return;
    int cx = x;
    for (; *s; s++) {
        if (*s == '\n') { cx = x; y += 9; continue; }
        gfx_draw_char(ctx, cx, y, *s, fg, bg);
        cx += 8;
        if ((uint32_t)cx + 8 > ctx->width) { cx = x; y += 9; }
    }
}

gfx_surface_t *gfx_surface_create(uint32_t w, uint32_t h) {
    gfx_surface_t *surf = (gfx_surface_t *)gfx_malloc(sizeof(gfx_surface_t));
    if (!surf) return (gfx_surface_t *)0;
    surf->data = (uint32_t *)gfx_malloc((uint64_t)w * h * 4);
    if (!surf->data) return (gfx_surface_t *)0;
    surf->width  = w;
    surf->height = h;
    return surf;
}

void gfx_blit(gfx_ctx_t *ctx, gfx_surface_t *src, int dst_x, int dst_y) {
    if (!ctx || !src) return;
    for (uint32_t row = 0; row < src->height; row++) {
        int py = dst_y + (int)row;
        if (py < 0 || (uint32_t)py >= ctx->height) continue;
        for (uint32_t col = 0; col < src->width; col++) {
            int px = dst_x + (int)col;
            if (px < 0 || (uint32_t)px >= ctx->width) continue;
            uint32_t *dst_row = (uint32_t *)((uint8_t *)ctx->pixels +
                                              (uint64_t)py * ctx->pitch);
            dst_row[px] = src->data[row * src->width + col];
        }
    }
}

