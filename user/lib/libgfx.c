void gfx_surface_blit_rect_to_ctx(gfx_ctx_t *ctx, gfx_surface_t *src,
                                    int sx, int sy, int sw, int sh, int dx, int dy) {
    if (!ctx || !src) return;
    for (int row = 0; row < sh; row++) {
        int srow = sy + row;
        int drow = dy + row;
        if (srow < 0 || (uint32_t)srow >= src->height) continue;
        if (drow < 0 || (uint32_t)drow >= ctx->height) continue;
        uint32_t *dst_row = (uint32_t *)((uint8_t *)ctx->pixels + (uint64_t)drow * ctx->pitch);
        for (int col = 0; col < sw; col++) {
            int scol = sx + col;
            int dcol = dx + col;
            if (scol < 0 || (uint32_t)scol >= src->width) continue;
            if (dcol < 0 || (uint32_t)dcol >= ctx->width) continue;
            dst_row[dcol] = src->data[(uint64_t)srow * src->width + scol];
        }
    }
}
